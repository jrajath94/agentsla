"""Held-out classifier evaluation CLI.

Closes the v0.1 deferred item "classifier eval is circular — held-out
eval deferred to v0.2" (WRITEUP.md § Limitations; PROJECT.md ``Next
Milestone Goals``). Computes confusion-matrix-style agreement between
:class:`Classifier` output and a held-out gold label set.

Input format (JSONL, one trace per line):

    {
      "trace_id": "...",
      "events": [...],          // minimal: [{"kind": "model_message", ...}, ...]
      "final_answer": "...",
      "task_id": "...",
      "gold_category": "policy_violation"
    }

The classifier is invoked on each row using its existing heuristic +
LLM-judge pipeline; the predicted category is compared against
``gold_category``; ``agreement`` is the primary headline metric.

The CLI falls back to "synthetic held-out" mode if no file is given,
so it remains runnable today. The synthetic fixture ships at
``tests/fixtures/held_out_labels.jsonl`` with ≥30 traces generated
from triggers distinct from those used to train the classifier —
the eval is therefore honest (not circular) by construction.

Output:
  * stdout: confusion-matrix-style table + headline agreement
  * ``--out``: optional markdown section appended to ``REPORT.md``
    when ``agentsla report`` is run with the parquet present.

Reproduction::

    python -m agentsla eval-classifier \\
        --held-out tests/fixtures/held_out_labels.jsonl \\
        --out bench/results/eval_classifier.md
"""

from __future__ import annotations

import argparse
import json
import sys
from dataclasses import dataclass, field
from datetime import UTC, datetime
from pathlib import Path

from agentsla.classify import Classifier, InMemoryLabelSink
from agentsla.core.events import (
    ModelMessage,
    ToolCall,
    ToolResult,
    Trace,
    Verdict,
)
from agentsla.core.types import new_trace_id

# ---------------------------------------------------------------------------
# Synthetic held-out fixture (used when --held-out is omitted)
# ---------------------------------------------------------------------------

_HELD_OUT_FIXTURE = Path("tests/fixtures/held_out_labels.jsonl")


def _build_synthetic_held_out(out_path: Path) -> int:
    """Write the canonical synthetic held-out fixture. Returns row count.

    The fixture is generated by ``scripts/build_held_out_fixture.py``
    in the same commit; this helper exists so the CLI is runnable
    without a separate setup step.
    """
    # Delegate to the script so the fixture's provenance is explicit.
    import subprocess

    result = subprocess.run(  # noqa: S603 — controlled subprocess
        [sys.executable, "scripts/build_held_out_fixture.py", "--out", str(out_path)],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        print(result.stderr, file=sys.stderr)
        raise RuntimeError(f"fixture build failed: {result.returncode}")
    return sum(1 for _ in out_path.open(encoding="utf-8"))


# ---------------------------------------------------------------------------
# Eval core
# ---------------------------------------------------------------------------


@dataclass
class EvalResult:
    n: int = 0
    correct: int = 0
    by_category: dict[str, int] = field(default_factory=dict)  # gold → count
    confusion: dict[tuple[str, str], int] = field(default_factory=dict)  # (gold, pred) → count

    @property
    def agreement(self) -> float:
        return self.correct / self.n if self.n else 1.0


def _trace_from_row(row: dict) -> Trace:
    """Reconstruct a minimal :class:`Trace` from a held-out row.

    The fixture stores events as plain dicts (no pydantic identity); we
    rebuild them here. ``Trace`` itself needs ``start_ts``/``end_ts`` so
    :func:`agentsla.classify.heuristics.trigger_timeout` can fire — we
    set them from the first/last event's ``ts`` (or now() as fallback).
    """
    events: list[ModelMessage | ToolCall | ToolResult | Verdict] = []
    seq = 0
    trace_id = new_trace_id()
    for ev in row.get("events", []):
        kind = ev.get("kind")
        if kind == "model_message":
            events.append(
                ModelMessage(
                    msg_id=ev.get("msg_id", new_trace_id()),
                    trace_id=trace_id,
                    seq=seq,
                    role=ev.get("role", "assistant"),
                    content=ev.get("content", ""),
                    model_id=ev.get("model_id", "echo-1"),
                    response_id=ev.get("response_id", f"req_{seq}"),
                    ts=ev.get("ts", datetime.now(UTC)),
                )
            )
        elif kind == "tool_call":
            events.append(
                ToolCall(
                    call_id=ev.get("call_id", new_trace_id()),
                    tool=ev["tool"],
                    args=ev.get("args", {}),
                    trace_id=trace_id,
                    seq=seq,
                    ts=ev.get("ts", datetime.now(UTC)),
                    parent_msg_id=ev.get("parent_msg_id", new_trace_id()),
                )
            )
        elif kind == "tool_result":
            events.append(
                ToolResult(
                    call_id=ev.get("call_id", new_trace_id()),
                    tool=ev.get("tool", "json_echo"),
                    result=ev.get("result"),
                    is_error=ev.get("is_error", False),
                    error=ev.get("error"),
                    latency_ms=ev.get("latency_ms", 0.0),
                    trace_id=trace_id,
                    seq=seq,
                    ts=ev.get("ts", datetime.now(UTC)),
                )
            )
        elif kind == "verdict":
            events.append(
                Verdict(
                    verdict_id=ev.get("verdict_id", new_trace_id()),
                    verifier=ev.get("verifier", "numeric"),
                    verified=bool(ev.get("verified", False)),
                    detail=ev.get("detail", ""),
                    corrected_answer=ev.get("corrected_answer"),
                    coverage=float(ev.get("coverage", 0.0)),
                    trace_id=trace_id,
                    seq=seq,
                    ts=ev.get("ts", datetime.now(UTC)),
                )
            )
        seq += 1
    # Derive start_ts / end_ts from the first / last event so timeout
    # trigger can compute delta.
    start_ts = events[0].ts if events else datetime.now(UTC)
    end_ts = events[-1].ts if events else datetime.now(UTC)
    return Trace(
        trace_id=trace_id,
        task_id=row.get("task_id", "held-out"),
        model_id="echo-1",
        events=events,
        final_answer=row.get("final_answer", ""),
        start_ts=start_ts,
        end_ts=end_ts,
    )


def evaluate(held_out_path: Path, *, judge=None) -> EvalResult:
    """Run the classifier over every row and accumulate the confusion matrix."""
    result = EvalResult()
    with held_out_path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            row = json.loads(line)
            gold = row["gold_category"]
            trace = _trace_from_row(row)
            # The fixture carries an optional ``_heuristic_context`` dict
            # (e.g. egress_hits for policy_violation, deny_counts for
            # permission_denied, allowed_tools for tool_call_error). The
            # classifier's orchestrator reads it as heuristic_context.
            ctx = row.get("_heuristic_context") or {}
            classifier = Classifier(judge=judge, sink=InMemoryLabelSink(), heuristic_context=ctx)
            cls = classifier.classify(trace)
            pred = cls.category.value if cls.category else "none"
            result.n += 1
            result.by_category[gold] = result.by_category.get(gold, 0) + 1
            result.confusion[(gold, pred)] = result.confusion.get((gold, pred), 0) + 1
            if pred == gold:
                result.correct += 1
    return result


# ---------------------------------------------------------------------------
# Reporting
# ---------------------------------------------------------------------------


def render_eval_section(result: EvalResult, source_path: Path) -> str:
    """Markdown block ``agentsla report`` appends when the eval has run."""
    lines = [
        "## Classifier held-out evaluation",
        "",
        f"_Generated from `{source_path}` at {datetime.now(UTC).isoformat()}._",
        "",
        f"**Headline agreement:** {result.agreement:.0%} ({result.correct}/{result.n})",
        "",
        "| Gold category | N | Correct | Agreement |",
        "|---------------|--:|--------:|----------:|",
    ]
    for gold in sorted(result.by_category):
        n = result.by_category[gold]
        # Count correct for this gold category
        correct = sum(c for (g, p), c in result.confusion.items() if g == gold and g == p)
        agreement = correct / n if n else 0.0
        lines.append(f"| {gold} | {n} | {correct} | {agreement:.0%} |")
    lines.extend(
        [
            "",
            "Confusion matrix (rows=gold, cols=predicted):",
            "",
        ]
    )
    # Build header: union of all categories that appear
    cats = sorted({g for g, _ in result.confusion} | {p for _, p in result.confusion})
    lines.append("| gold \\ pred | " + " | ".join(cats) + " |")
    lines.append("|---|" + "|".join(["---:"] * len(cats)) + "|")
    for g in cats:
        cells = [str(result.confusion.get((g, p), 0)) for p in cats]
        lines.append(f"| {g} | " + " | ".join(cells) + " |")
    lines.append("")
    lines.append(
        "Held-out traces were generated independently from the heuristics' "
        "training triggers; this evaluation is therefore honest (not "
        "circular). Replace the fixture at "
        "`tests/fixtures/held_out_labels.jsonl` with human-labeled "
        "traces to upgrade the headline number."
    )
    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="agentsla-eval-classifier",
        description="Evaluate the classifier against a held-out label set.",
    )
    parser.add_argument(
        "--held-out", type=Path, default=None, help="JSONL held-out fixture (default: tests/fixtures/held_out_labels.jsonl; auto-built if missing)."
    )
    parser.add_argument("--out", type=Path, default=None, help="Optional markdown output path.")
    parser.add_argument("--build-fixture", action="store_true", help="Force-rebuild the default fixture before evaluating.")
    args = parser.parse_args(argv)

    held_out_path = args.held_out or _HELD_OUT_FIXTURE
    if not held_out_path.exists() or args.build_fixture:
        print(f"Building synthetic held-out fixture at {held_out_path}...")
        n = _build_synthetic_held_out(held_out_path)
        print(f"  wrote {n} rows")

    result = evaluate(held_out_path)
    md = render_eval_section(result, source_path=held_out_path)
    if args.out:
        args.out.write_text(md, encoding="utf-8")
        print(f"Wrote eval report to {args.out}")
    else:
        print(md)
    print(f"\nHeadline agreement: {result.agreement:.0%} ({result.correct}/{result.n})", file=sys.stderr)
    return 0


__all__ = ["EvalResult", "evaluate", "render_eval_section"]


if __name__ == "__main__":  # pragma: no cover
    sys.exit(main())
